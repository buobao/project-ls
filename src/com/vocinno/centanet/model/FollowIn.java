package com.vocinno.centanet.model;

public class FollowIn {
	private String content="";	//跟进内容
	private String tracktime="";//跟进时间
	private String trackCat="";//跟进类别
	
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTracktime() {
		return tracktime;
	}
	public void setTracktime(String tracktime) {
		this.tracktime = tracktime;
	}
	public String getTrackCat() {
		return trackCat;
	}
	public void setTrackCat(String trackCat) {
		this.trackCat = trackCat;
	}

}
